class Location {
  final String area;
  final String block;
  final String city;
  final String code;
  final String country;
  final String district;
  final String googleMap;
  final String house;
  final String road;

  Location({
    required this.area,
    required this.block,
    required this.city,
    required this.code,
    required this.country,
    required this.district,
    required this.googleMap,
    required this.house,
    required this.road,
  });
}